		<center><h1>¿Qué es Interfaz de usuario?</h1>
		 <img src="views/modules/img/ui.jpg" width="55%" height="300px" style="border-radius: 10px">
	<p>Es el conjunto de los controles y canales sensoriales mediante los cuales un usuario puede comunicarse con una máquina. Por ejemplo, en una computadora, la pantalla, el teclado y las bocinas son parte de la interfaz de usuario porque la utilidad de todas ellas es hacer que entre o salga información del equipo.</p>
		
		<h1>¿Qué es la interfaz gráfica de usuario (GUI)?</h1>
		<img src="views/modules/img/gui.png" width="55%" height="300px" style="border-radius: 10px">
	<p>Es el contenido gráfico mediante el cual se visualiza información del equipo en una pantalla. Casi todos los programas tienen alguna clase de interfaz visual, que sirve al mismo tiempo para mostrar información al usuario y como un mapa de navegación entre diferentes comandos.
	Por ejemplo, si abres un archivo de Word, la interfaz de usuario te permitirá ver la información que contiene en la pantalla, pero también te permitirá interactuar con un sinfín de herramientas para modificar el documento.
	</p>	

		<h1>¿Qué es la interfaz de usuario de un sitio web?</h1>
		<img src="views/modules/img/web.png" width="55%" height="300px" style="border-radius: 10px">
	<p>Se refiere a todos los gráficos, información y herramientas que aparecen en la pantalla al abrir una página web. Aunque estos elementos funcionan de manera muy similar a otros programas de computadora, tienen que ser mucho más intuitivos y fáciles de usar, pues sería muy poco práctico que los internautas tuvieran que invertir tiempo aprendiendo a manejar los comandos de cada sitio web que visitan.
	</p>

		<h1>Otros tipos de interfaz de usuario</h1>
		<img src="views/modules/img/types.png" width="55%" height="300px " style="border-radius: 10px">
	<p>Aunque la mayoría de nosotros estamos acostumbrados a interfaces de tipo gráficas, existen otras formas de comunicación entre un software y un usuario:</p>
		<h2>Interfaz de voz (VUI):</h2> <p>Se trata de programas capaces de identificar e interpretar el habla. El ejemplo más claro que tenemos es el reciente auge de las inteligencias artificiales, como Siri, que se controlan por medio de la voz.</p>
		<h2>Interfaz de texto:</h2> <p>Se utiliza principalmente en el ámbito de la programación de sistemas operativos y es la evolución de la interfaz de línea de comando primitiva que usaban los primeros programas de computadora.</p>
		<h2>Interfaz natural:</h2><p> Se le llama así al tipo de interfaz que identifica e interpreta acciones naturales del ser humano, como movimientos y expresiones faciales. Un ejemplo de ello son los videojuegos con interfaz kinética.</p>
		<h2>Interfaz cerebro-ordenador:</h2><p> Es el tipo de interfaz más innovadora que existe hasta el momento, y aunque aún no cuenta con muchas aplicaciones cotidianas, se está utilizando para controlar prótesis biónicas y dar instrucciones sencillas a un software por medio de las ondas cerebrales.<p>

		<h1>Principios</h1>
		<img src="views/modules/img/principios.jpg" width="80%" height="150px" style="border-radius: 10px">
			<h2>1. La claridad es el trabajo</h2>
	<p>La claridad es el trabajo primero y más importante de cualquier interfaz. La claridad inspira confianza y conduce a un uso posterior. Cien pantallas claras son preferible a un solo desorden.</p>
		<h2>2. Conserve la atención a toda costa</h2>
	<p>Vivimos en un mundo de interrupciones. Es difícil leer tranquilamente y en paz sin distraernos y dirigir nuestra atención a un solo punto. La atención es una prioridad. Como por el caso de ella publicidad que no debería ensuciar los espacios laterales de las aplicaciones con material que distraiga la atención se convierte en el requisito previo. Así que hay que conservarla a toda costa.</p>
		<h2>4. Mantener a los usuarios bajo control</h2>
	<p>Los seres humanos se encuentran más cómodos de sí mismos y de su entorno cuando se sienten bajo control.  Hay que mantenga a los usuarios bajo control mediante la aparición periódica del estado del sistema, describiendo la causalidad (si usted hace esto que va a suceder) y dando una idea de lo que debe esperar en cada vuelta.</p>
		<h2>5. Una acción primaria por pantalla</h2>
	<p>Cada pantalla que diseñemos debe soportar una sola acción de valor real para la persona que la usa. Esto hará que sea más fácil de aprender, más fácil de usar y más fácil de agregar o construir cuando sea necesario.</p>
		<h2>6. Proporcionar un paso siguiente natural</h2>
	<p>Muy pocas interacciones están destinadas a ser la última, así que hay que diseñar cuidadosamente el próximo paso para cada interacción que una persona tiene que realizar con su interfaz. Anticipar y diseñar para apoyarla, lo que la próxima interacción debería ser y/o suceder. </p>
		<h2>7. Organización inteligente reduce la carga cognitiva</h2>
	<p>Una organización inteligente de los elementos de pantalla puede hacer que los muchos aparezcan como los pocos. Esto ayudará a los usuarios a entender nuestra interfaz más fácil y más rápidamente, ya que se ha ilustrado las relaciones inherentes de contenido en su diseño. Agrupar como elementos, mostrar las relaciones naturales por la colocación y la orientación</p>
		<h2>8. Resaltar, no determinar, con el color</h2>
	<p>Al igual que en el mundo físico, donde el color es muy subjetivo, el color no debe determinar mucho en una interfaz. Puede ayudar, ser utilizado para destacar, ser utilizado para guiar la atención, pero no debe ser el único diferenciador de las cosas.</p>
		<h2>9. Divulgación progresiva</h2>
	<p>Mostrar sólo lo que es necesario en cada pantalla. Si los usuarios están haciendo una elección, se debe mostrar suficiente información para permitirles la elección, luego se entrará en detalles en una pantalla posterior. </p>
		<h2>10. Construir en otras disciplinas de diseño</h2>
	<p>Diseño visual y gráfico, tipografía, copywriting, arquitectura de información y visualización todas estas disciplinas forman parte del diseño de interfaces.</p>

