		<h2>Estos son los cambios en la interfaz de Facebook: </h2>
		<h3>Mejor experiencia con los comentarios</h3>
	<p>Facebook ha optado por hacer un poco más dinámico todo lo relacionado con los comentarios para que puedas obtener más información con sólo un vistazo a la publicación en cuestión que estés mirando.
	Además, los iconos también han cambiado, y la posición de los botones en la barra de respuestas hará que comentar en una publicación sea más simple que nunca.</p>
