		<h3>Una navegación más cómoda</h3>
	<p>Otro cambio bastante notable vamos a encontrarlo en el feed de Facebook, el cual deja atrás el enorme encabezado que encontrábamos cuando navegábamos por un perfil para incluir ahí la fotografía del perfil en cuestión y que tengamos más información disponible en la pantalla.
	Además, los botones de “Me gusta”, “Compartir” y “Comentar” son más grandes y su diseño es más minimalista, lo cual nunca viene nada mal.</p>
